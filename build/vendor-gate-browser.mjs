// Role: vendor the v3 gate kernel into one browser script so the compose-gate panel runs the actual
//   kernel in a file:// page, with no network and no Python. The kernel .mjs modules are the single
//   source of truth; this concatenates them (import graph resolved to a local registry) into
//   vendor/gate/gate.bundle.js, exposed as window.EpiGate. It rewrites nothing but the module
//   boundary, so the browser runs byte-for-byte the same logic the oracles check headless.
// Contract: `node build/vendor-gate-browser.mjs` writes the generated bundle. No arguments.
// Invariant: the kernel stays the forkable artifact; this is a build artifact regenerated from it,
//   never hand-edited. Named imports and `export function`/`export const` only (the kernel's forms).
"use strict";
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join, basename } from "node:path";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");

// the gate path, in dependency order (bottom-up). Keyed by basename, which is unique across the set.
const MODULES = [
  "kernel/schema/sha256.mjs",
  "kernel/schema/confidence.mjs",
  "kernel/schema/canonical.mjs",
  "kernel/schema/tables.mjs",
  "kernel/schema/records.mjs",
  "kernel/grounding/earned-grade.mjs",
  "kernel/store/state.mjs",
  "kernel/store/apply.mjs",
  "kernel/store/decay.mjs",
  "kernel/gate/gate.mjs",
  // the API layer: the local provider (the one api module that imports the kernel), the stub remote
  // provider (imports no kernel), and the contract. Bundled so the propose/read path runs client-
  // side and the provider swap is demonstrable; kernel imports resolve to __M.
  "api/providers/local-provider.mjs",
  "api/providers/remote-provider.mjs",
  "api/client-api.mjs",
];
// the surface the compose-gate panel consumes.
const EXPOSE = ["claimRecord", "linkRecord", "makeSourceTable", "makeKindTable", "genesis", "apply", "storeViewOf", "decide", "hashOf"];

const key = (p) => basename(p, ".mjs");

// parse one module: its named imports (name -> source module key), its exported names, and the body
// with import lines removed and the `export ` keyword stripped from declarations.
function parseModule(relPath) {
  const src = readFileSync(join(ROOT, relPath), "utf8");
  const imports = []; // { names:[...], from:key }
  const exportNames = [];
  const lines = src.split("\n");
  const bodyLines = [];
  for (const line of lines) {
    const imp = line.match(/^import\s*\{([^}]*)\}\s*from\s*"([^"]+)";?\s*$/);
    if (imp) {
      const names = imp[1].split(",").map((s) => s.trim()).filter(Boolean);
      imports.push({ names, from: key(imp[2]) });
      continue; // drop the import line; wired via the registry instead
    }
    const exp = line.match(/^export\s+(function|const|let|var|class)\s+([A-Za-z0-9_$]+)/);
    if (exp) {
      exportNames.push(exp[2]);
      bodyLines.push(line.replace(/^export\s+/, ""));
      continue;
    }
    bodyLines.push(line);
  }
  return { key: key(relPath), imports, exportNames, body: bodyLines.join("\n") };
}

const parsed = MODULES.map(parseModule);

// emit: a registry of module-IIFEs, each fed its imports from the registry, returning its exports.
let out = "";
out += "// GENERATED by build/vendor-gate-browser.mjs from the kernel .mjs modules. Do not edit.\n";
out += "// The v3 gate kernel, the same source the oracles check, bundled for a file:// page.\n";
out += "(function (root) {\n\"use strict\";\nvar __M = {};\n";
for (const m of parsed) {
  const destructure = m.imports
    .map((i) => `  var { ${i.names.join(", ")} } = __M[${JSON.stringify(i.from)}];`)
    .join("\n");
  out += `__M[${JSON.stringify(m.key)}] = (function () {\n`;
  if (destructure) out += destructure + "\n";
  out += m.body + "\n";
  out += `  return { ${m.exportNames.join(", ")} };\n`;
  out += "})();\n";
}
// map each exported name to the module that exports it, so exposure needs no hand-maintained table.
const exportedBy = {};
for (const m of parsed) for (const n of m.exportNames) exportedBy[n] = `__M[${JSON.stringify(m.key)}]`;
const exposeObj = (names) => "{\n" + names.map((n) => `  ${n}: ${exportedBy[n]}.${n}`).join(",\n") + "\n}";
// EpiGate: the raw kernel surface (the compose-gate panel consumes it). EpiClientApi / EpiLocalProvider:
// the propose/read contract and the local provider, so the client runs the real gate in the page.
out += "root.EpiGate = " + exposeObj(EXPOSE) + ";\n";
out += "root.EpiClientApi = " + exposeObj(["createClientApi"]) + ";\n";
out += "root.EpiLocalProvider = " + exposeObj(["createLocalProvider"]) + ";\n";
out += "root.EpiRemoteProvider = " + exposeObj(["createRemoteProvider"]) + ";\n";
out += "})(typeof window !== \"undefined\" ? window : globalThis);\n";

const dest = join(ROOT, "vendor/gate/gate.bundle.js");
writeFileSync(dest, out);
console.log(`wrote vendor/gate/gate.bundle.js (${out.length} bytes) from ${MODULES.length} modules (kernel + api provider/contract)`);
